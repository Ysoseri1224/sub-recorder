export function t(key: string, locale?: Locale, fallback?: string): string {
  const l = locale || getLocale();
  return translations[l]?.[key] ?? translations["en"]?.[key] ?? fallback ?? key;
}

export function useTranslations() {
  if (typeof window === "undefined") {
    return {
      t: (key: string, fallback?: string) => t(key, "en", fallback),
      locale: "en" as Locale,
    };
  }
  const locale = getLocale();
  return {
    t: (key: string, fallback?: string) => t(key, locale, fallback),
    locale,
  };
}
